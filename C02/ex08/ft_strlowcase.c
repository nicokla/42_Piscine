char	*ft_strlowcase(char *str)
{
	int		i;
	char	*str0;

	i = 0;
	str0 = str;
	while (*str != '\0' && i < 1000000)
	{
		if (*str >= 65 && *str <= 90)
		{
			*str = *str + 32;
		}
		str++;
		i++;
	}
	return (str0);
}
