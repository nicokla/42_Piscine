char	*ft_strupcase(char *str)
{
	int		i;
	char	*str0;

	i = 0;
	str0 = str;
	while (*str != '\0' && i < 1000000)
	{
		if (*str >= 97 && *str <= 122)
		{
			*str = *str - 32;
		}
		str++;
		i++;
	}
	return (str0);
}
