#include <unistd.h>

void	ft_putstr(char *s)
{
	while (*s != '\0')
	{
		write(1, s, 1);
		s++;
	}
}

void	ft_putstr2(char *s, int taille)
{
	int i;

	i = 0;
	while (i < taille)
	{
		write(1, s + i, 1);
		i++;
	}
}
