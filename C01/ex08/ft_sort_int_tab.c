void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int j;
	int x;

	i = 1;
	while (i < size)
	{
		x = tab[i];
		j = i;
		while (j > 0 && tab[j - 1] > x)
		{
			tab[j] = tab[j - 1];
			j--;
		}
		tab[j] = x;
		i++;
	}
}
