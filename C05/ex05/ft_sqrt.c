int		ft_sqrt(int nb)
{
	int i;
	int result;

	if (nb < 0)
		return (0);
	i = 0;
	result = 0;
	while (result < nb && i <= 46340)
	{
		i++;
		result = i * i;
	}
	if (result == nb)
		return (i);
	else
		return (0);
}
